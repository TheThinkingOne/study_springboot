CREATE TABLE `COMPANY` (
    `CP_PK` VARCHAR(20) NOT NULL,
    `CP_NAME` VARCHAR(20) NULL,
    PRIMARY KEY (`CP_PK`)
);
CREATE TABLE `CAR_NAME` (
    `CN_PK` VARCHAR(20) NOT NULL,
    `CAR_NAME` VARCHAR(20) NULL,
    PRIMARY KEY (`CN_PK`)
);
CREATE TABLE `MODEL_YEAR` (
    `YEAR_PK` VARCHAR(20) NOT NULL,
    `DATE_YEAR` VARCHAR(20) NULL,
    PRIMARY KEY (`YEAR_PK`)
);
CREATE TABLE `CAR_INFO` (
    `CI_PK` VARCHAR(20) NOT NULL,
    `CP_FK` VARCHAR(20) NOT NULL,
    `CN_FK` VARCHAR(20) NOT NULL,
    `YEAR_FK` VARCHAR(20) NOT NULL,
    PRIMARY KEY (`CI_PK`),
    FOREIGN KEY (`CP_FK`) REFERENCES `COMPANY` (`CP_PK`),
    FOREIGN KEY (`CN_FK`) REFERENCES `CAR_NAME` (`CN_PK`),
    FOREIGN KEY (`YEAR_FK`) REFERENCES `MODEL_YEAR` (`YEAR_PK`)
);
CREATE TABLE `OPTIONS` (
    `OP_PK` VARCHAR(20) NOT NULL,
    `OPTION` VARCHAR(20) NULL,
    PRIMARY KEY (`OP_PK`)
);
CREATE TABLE `CAR_INFO_OPTIONS` (
    `CI_OP_PK` VARCHAR(20) NOT NULL,
    `OP_FK` VARCHAR(20) NOT NULL,
    `CI_FK` VARCHAR(20) NOT NULL,
    PRIMARY KEY (`CI_OP_PK`),
    FOREIGN KEY (`OP_FK`) REFERENCES `OPTIONS` (`OP_PK`),
    FOREIGN KEY (`CI_FK`) REFERENCES `CAR_INFO` (`CI_PK`)
);